package Com.Bridgelabz.Adress;

public class UsingNameUC3 {

	    private String name;
	    private int age;

	    public UsingNameUC3(String initialName) {
	        this.age = 0;
	        this.name = initialName;
	    }

	    public void printPerson() {
	        System.out.println(this.name + ", age " + this.age + " years");
	    }
}

